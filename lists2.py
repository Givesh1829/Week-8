
fruits = ["apple", "banana", "cherry"]

fruits.append("kiwi") #if you want to add a value to the list.
print(fruits)

fruits.insert(1, "orange")  #if you want to add a value to a certain position.
print(fruits)

fruits.remove("kiwi") #if you want to remove a value from the list.
print(fruits)

fruits.sort(reverse=True) #if you want to sort your list out in acending order by default. if you want decending order you have to use(reverse=True).
print(fruits)